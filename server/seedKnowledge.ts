/**
 * تغذية قاعدة المعرفة بالبيانات الأساسية
 */
export async function seedKnowledgeBase(): Promise<{ success: number; failed: number; errors: string[] }> {
  let success = 0;
  let failed = 0;
  const errors: string[] = [];

  console.log(`[SeedKnowledge] Starting to seed ${KNOWLEDGE_ENTRIES.length} knowledge entries...`);

  for (const entry of KNOWLEDGE_ENTRIES) {
    const result = await addKnowledge({
      domain: entry.domain,
      category: entry.category,
      title: entry.title,
      content: entry.content,
      keywords: entry.keywords,
      source: entry.source,
      addedBy: 'system_seed',
    });

    if (result.success) {
      success++;
      console.log(`[SeedKnowledge] ✓ Added: ${entry.title} (ID: ${result.id})`);
    } else {
      failed++;
      errors.push(`${entry.title}: ${result.error}`);
      console.error(`[SeedKnowledge] ✗ Failed: ${entry.title} - ${result.error}`);
    }
  }

  console.log(`[SeedKnowledge] Done. Success: ${success}, Failed: ${failed}`);
  return { success, failed, errors };
}
